use smart_leds::colors::*;

macro_rules! color_wheel {
    ( $const_array:ident, $name_array:ident, [ $( $const:ident ),* ] ) => {
        pub const $const_array: [smart_leds::RGB8; count_idents!($( $const ),*)] = [ $( $const ),* ];
        pub const $name_array: [&'static str; count_idents!($( $const ),*)] = [ $( stringify!($const) ),* ];
    };
}

macro_rules! count_idents {
    ( $($idents:ident),* ) => {
        <[()]>::len(&[$(count_idents!(@substitute $idents)),*])
    };
    (@substitute $_ident:ident) => { () };
}

